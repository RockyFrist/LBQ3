// ===== 去重系统：记住每名弟子最近的台词 =====
const _recent = new Map();
function _pick(pool, id, maxRecent) {
  if (!pool || !pool.length) return '……';
  const r = _recent.get(id) || [];
  const available = pool.filter(l => !r.includes(l));
  const src = available.length > 0 ? available : pool;
  const line = src[Math.floor(Math.random() * src.length)];
  _recent.set(id, [line, ...r].slice(0, maxRecent || 14));
  return line;
}

/** 清除某弟子的台词历史（升级/退出时调用） */
export function resetDialogueMemory(id) {
  _recent.delete(id);
}

/**
 * 根据弟子状态自动选择上下文并取台词
 * @param {object} disciple - 弟子对象
 * @param {string} [ctx='auto'] - 强制上下文; 'auto' 时自动推断
 */
export function pickDialogueLine(disciple, ctx) {
  const p = disciple.personality || 'diligent';
  let context = ctx || 'auto';
  if (context === 'auto') {
    if (disciple.level >= disciple.talent)    context = 'mastered';
    else if ((disciple.stamina || 100) < 15)  context = 'exhausted';
    else if ((disciple.stamina || 100) < 35)  context = 'tired';
    else if ((disciple.injury  || 0)  > 30)   context = 'injured';
    else if ((disciple.loyalty || 50) >= 82)  context = 'inspired';
    else                                       context = 'normal';
  }
  const bucket = _D[p] || _D.diligent;
  const pool = bucket[context] || bucket.normal || [];
  return _pick(pool, disciple.id);
}

/**
 * 为全体训练选出若干发言弟子
 * @param {object[]} disciples
 * @param {number} [count=3]
 */
export function pickGroupSpeakers(disciples, count) {
  if (!disciples || !disciples.length) return [];
  const n = Math.min(count || 3, disciples.length);
  const shuffled = [...disciples].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, n).map((d, i) => ({
    disciple: d,
    line: pickDialogueLine(d, 'auto'),
    showAt: 0.44 + i * 0.18,
  }));
}

/** 向后兼容旧接口 */
export function pickTrainLine(disciple, context) {
  const ctx = context === 'levelup' ? 'levelup'
    : context === 'intense' ? 'intense'
    : 'auto';
  return pickDialogueLine(disciple, ctx);
}

